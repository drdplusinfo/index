      <h1 class="heading-without-id">Asterion</h1>

      <img class="big-picture" src="/images/projects/asterion.jpg" alt="Asterion">

      <p>Historie rozsáhlého herního světa Asterionu sahá až do roku 1999. Tehdy světu vládlo Dračí doupě, všechno
        bylo nové a prostoupené ryzím nadšením.</p>

      <p>U zrodu Asterionu stáli Jiří "Jersey" Buchta a Karel Makovský, o pár let později pak tento svět
        <a href="http://www.sarden.cz/2016-10-10-0348/rozhovor-s-janem-c-galetou-nejen-o-asterionu">převzali</a> jejich
        následovníci. A Asterion žije <a href="http://asterionrpg.cz/">dodnes</a>.</p>

      <p>Moduly Asterionu vychází pod různými vydavatelstvími, podle aktuální situace, takže sehnat všechny může být
        trochu oříšek. První moduly vydával a stále prodává
        <a href="https://obchod.altar.cz/draci-doupe-a-jine-rpg-hry-asterion-c-1_11.html">Altar</a>, další vydaly
        <a href="https://www.straky.cz/katalog.aspx?Action=Author&AuthID=33&AuthorName=Zbyn%c4%9bk+Ku%c4%8dera+Holub">Straky
          na vrbě</a> a nejnovější vydává <a href="http://www.mytago.cz/books/">Mytago</a> a prodává
        <a href="https://www.imago.cz/draci-doupe-asterion-c-8_97_99_586_104.html">Imago</a>.
      </p>

      <p>Ke světu Asterionu někteří přilnuli i spisovatelsky. Nejobsáhlejší autor bude zřejmě Hanina Veselá a její
        alter ego, psionička <a href="http://haninavesela.cz/category/cyklus-magnolie/">Magnólie</a>, která se páře leda
        s břichy nepřátel.</p>

      <p><i>Komu vyhovuje hotový obsah, propracované politické pozadí, barevné mapy a vůbec dobrodružství, pro které
          nemusí už nic vymýšlet, ten bude s Asterionem spokojen.</i></p>

      <p>Více detailů o Asterionu se dozvíte na <a href="http://asterionrpg.cz/">Asterionrpg.cz</a>.</p>

